class Book:
    def __init__(self, title: str, author: str, year: int, price: float, genre: str, stock: int, description: str, isbn: int):
        self.title = title
        self.author = author
        self.year = year
        self.price = price
        self.genre = genre
        self.stock = stock
        self.isbn = isbn
        self.description = description
